# GENERAL IDEA
# Functions group reusable logic into a named block of code.
# Parameters let functions accept inputs.
# Named parameters let you control behavior without relying on position.
# *args allows any number of positional arguments.
# **kwargs allows any number of named arguments.

# BASIC SYNTAX
# def function_name(parameters):
#     code block
#     return value   # optional

